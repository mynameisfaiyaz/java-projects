public class Keywords {

    public static void main(String[] args) {
        //anything in blue is reserved keywords
        //anything under a parenthesis is expression
        //whatever line has operator is expression
        //line which ends with ; is a statement
        int myVariable = 45;
        myVariable--;//decrements myVariable by 1
        myVariable++;//increments myVariable by 1
        myVariable++;
        myVariable--;
        System.out.println(myVariable);
        System.out.println(myVariable + 1);//adds 1 to the value of myVariable
    }
}
